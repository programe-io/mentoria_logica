package aula_01;

public class TiposDados {
    public static void main(String[] args) {

        /* Declaração das variáveis numero1 e numero2 ambas do tipo int (inteiro), e recebem os valores literais 10 e 3, respectivamente. */
        int numero1 = 10;
        int numero2 = 3;


        /* Declaramos a variável resultado1 (do tipo inteiro) que recebe o valor resultante da divisão ( operador / ) de numero1 por numero2 */
        int resultado1 = numero1 / numero2;

        /* Declaramos a variável resultado2 (do tipo inteiro) que recebe o valor resultante do resto da divisão ( operador % ) de numero1 por numero2 */
        int resultado2 = numero1 % numero2;


        /* Impressões dos resultados na tela. */
        System.out.println(resultado1);
        System.out.println(resultado2);
    }
}

