package aula_01;

public class Constante {
    public static void main(String[] args) {

        /* Constante é uma espécie de "variável que não sobre alteração de valor. Na declaração da constante devemos informar o seu valor, e tentativas de inserir um novo valor resulta em erro." */

        /* Por padrão o nome de uma constante deve ser declarado em CAIXA_ALTA, e se houver mais de uma palavra são separadas por um underline _ */
        final int NUMERO_BASE = 10;

        /* Impressão do valor da constante NUMERO_BASE */
        System.out.println(NUMERO_BASE);
    }
}
