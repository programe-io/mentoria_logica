package _2024_10_30;

public class OperradoresRelacionais {
    public static void main(String[] args) {

        int valor1 = 15;
        int valor2 = 15;

        /* Operadores relacionais servem para analisar duas variáveis ou dois valores. A análise entre esses dois elementos pode ser de maior, menor, igualdade ou diferença. */
        boolean resultado = valor1 <= valor2;

        System.out.println(resultado);
    }
}
