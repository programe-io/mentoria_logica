package _2024_10_30;

public class TipoTexto {
    public static void main(String[] args) {

        char opcao = 'A';
        String nome = "Francisco Coelho";

        /* O Java não tem um tipo primitivo que guarde uma palavra (conjunto de caracteres). Para guardar esse conjunto de elementos char, deve-se utilizar a classe String. Importante lembrar que variáveis char guarda o caractere dentro de aspas simples '', enquanto a String guarda o valor textual dentro de aspas duplas "" */

        System.out.println(opcao);
        System.out.println(nome);
    }
}

